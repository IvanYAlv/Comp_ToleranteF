    public Fr() {
        initComponents();
        File file = new File("estudiantes.json");
        if (file.exists()) {
            es = j.read_JSON();
            txtcod.setText(es.getCod());
            txtnom.setText(es.getNom());
            txtape.setText(es.getApe());
            txtcen.setText(es.getCen());
            txttel.setText(es.getNum());
        }
    }

        public void salir() {
            if (empty()) {
                JOptionPane.showMessageDialog(this, "Bai");
            } else {
                es.setCod(txtcod.getText());
                es.setNom(txtnom.getText());
                es.setApe(txtape.getText());
                es.setCen(txtcen.getText());
                es.setNum(txttel.getText());
                j.write_JSON(es);
            }
        }

    boolean empty() {
        if (txtcod.getText().isEmpty() || txtnom.getText().isEmpty() || txtape.getText().isEmpty()
            || txtcen.getText().isEmpty() || txttel.getText().isEmpty()) {
            return true;
        } else {
            return false;
        }
    }

